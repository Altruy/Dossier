class Collaboration {
  constructor(id, name, users, notes, tasks, events, chats, notifications) {
    this.id = id;
    this.name = name;
    this.users = users;
    this.notes = notes;
    this.tasks = tasks;
    this.events = events;
    this.chats = chats;
    this.notifications = notifications;
  }
}

export default Collaboration;
